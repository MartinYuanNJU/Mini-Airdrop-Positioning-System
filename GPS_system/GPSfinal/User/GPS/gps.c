 //分析GSA信息
//gpsx:nmea信息结构体
//buf:接收到的GPS数据缓冲区首地址
void NMEA_GSA_Analysis(nmea_msg *gpsx,u8 *buf)
{
	u8 *p1,dx;			 
	u8 posx; 
	u8 i;   
	p1=(u8*)strstr((const char *)buf,"$GPGSA");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"$GNGSA");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"$GLGSA");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"$GBGSA");
	posx=Comma_Pos(p1,2);								//得到定位类型
	if(posx!=0XFF)gpsx->fixmode=Str2num(p1+posx,&dx);	
	if(gpsx->fixmode>3) gpsx->fixmode=0;
	for(i=0;i<12;i++)										//得到定位卫星编号
	{
		posx=Comma_Pos(p1,3+i);					 
		if(posx!=0XFF)gpsx->possl[i]=Str2num(p1+posx,&dx);
		else break; 
	}				  
	posx=Comma_Pos(p1,15);								//得到PDOP位置精度因子
	if(posx!=0XFF)gpsx->pdop=Str2num(p1+posx,&dx);  
	posx=Comma_Pos(p1,16);								//得到HDOP位置精度因子
	if(posx!=0XFF)gpsx->hdop=Str2num(p1+posx,&dx);  
	posx=Comma_Pos(p1,17);								//得到VDOP位置精度因子
	if(posx!=0XFF)gpsx->vdop=Str2num(p1+posx,&dx);  
}
//分析GSV信息
//gpsx:nmea信息结构体
//buf:接收到的GPS数据缓冲区首地址
void NMEA_GSV_Analysis(nmea_msg *gpsx,u8 *buf)
{
	u8 *p,*p1,dx;
	u8 len,i,j,slx=0;
	u8 posx;   	 
	p=buf;
	p1=(u8*)strstr((const char *)p,"$GPGSV");
	if (p1==NULL) p1=(u8*)strstr((const char *)p,"$GNGSV");
	if (p1==NULL) p1=(u8*)strstr((const char *)p,"$GLGSV");
	if (p1==NULL) p1=(u8*)strstr((const char *)p,"$GBGSV");
	len=p1[7]-'0';								//得到GPGSV的条数
	posx=Comma_Pos(p1,3); 					//得到可见卫星总数
	if(posx!=0XFF)gpsx->svnum=Str2num(p1+posx,&dx);
	for(i=0;i<len;i++)
	{	 
		p1=(u8*)strstr((const char *)p,"$GPGSV");
		if (p1==NULL) p1=(u8*)strstr((const char *)p,"$GNGSV");
		if (p1==NULL) p1=(u8*)strstr((const char *)p,"$GLGSV");
		if (p1==NULL) p1=(u8*)strstr((const char *)p,"$GBGSV");		
		for(j=0;j<4;j++)
		{	  
			posx=Comma_Pos(p1,4+j*4);
			if(posx!=0XFF)gpsx->slmsg[slx].num=Str2num(p1+posx,&dx);	//得到卫星编号
			else break; 
			posx=Comma_Pos(p1,5+j*4);
			if(posx!=0XFF)gpsx->slmsg[slx].eledeg=Str2num(p1+posx,&dx);//得到卫星仰角 
			else break;
			posx=Comma_Pos(p1,6+j*4);
			if(posx!=0XFF)gpsx->slmsg[slx].azideg=Str2num(p1+posx,&dx);//得到卫星方位角
			else break; 
			posx=Comma_Pos(p1,7+j*4);
			if(posx!=0XFF)gpsx->slmsg[slx].sn=Str2num(p1+posx,&dx);	//得到卫星信噪比
			else break;
			slx++;	   
		}   
 		p=p1+1;//切换到下一个GPGSV信息
	}   
}
//分析GGA信息
//gpsx:nmea信息结构体
//buf:接收到的GPS数据缓冲区首地址
void NMEA_GGA_Analysis(nmea_msg *gpsx,u8 *buf)
{
	u8 *p1,dx;			 
	u8 posx;    
	p1=(u8*)strstr((const char *)buf,"$GPGGA");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"$GNGGA");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"$GLGGA");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"$GBGGA");
	posx=Comma_Pos(p1,6);								//得到GPS状态
	if(posx!=0XFF)gpsx->gpssta=Str2num(p1+posx,&dx);	
	posx=Comma_Pos(p1,7);								//得到用于定位的卫星数
	if(posx!=0XFF)gpsx->posslnum=Str2num(p1+posx,&dx); 
	posx=Comma_Pos(p1,9);								//得到海拔高度
	if(posx!=0XFF && gpsx->fixmode>=2)gpsx->altitude=Str2num(p1+posx,&dx);  
}

//分析RMC信息
//gpsx:nmea信息结构体
//buf:接收到的GPS数据缓冲区首地址
void NMEA_RMC_Analysis(nmea_msg *gpsx,u8 *buf)
{
	u8 *p1,dx;			 
	u8 posx;     
	u32 temp;	   
	float rs;  
	p1=(u8*)strstr((const char *)buf,"GPRMC");//"$GPRMC",经常有&和GPRMC分开的情况,故只判断GPRMC.
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"GNRMC");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"GLRMC");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"GBRMC");
	posx=Comma_Pos(p1,1);								//得到UTC时间
	if(posx!=0XFF)
	{
		temp=Str2num(p1+posx,&dx)/Pow(10,dx);	 	//得到UTC时间,去掉ms
		gpsx->utc.hour=(temp/10000);
		gpsx->utc.min=(temp/100)%100;
		gpsx->utc.sec=temp%100;	 	 
	}	
	posx=Comma_Pos(p1,3);								//得到纬度
	if(posx!=0XFF && gpsx->fixmode>=2)
	{
		temp=Str2num(p1+posx,&dx);		 	 
		gpsx->latitude=temp/Pow(10,dx+2);	//得到°
		rs=temp%Pow(10,dx+2);				//得到'		 
		gpsx->latitude=gpsx->latitude*Pow(10,5)+(rs*Pow(10,5-dx))/60;//转换为° 
	}
	posx=Comma_Pos(p1,4);								//南纬还是北纬 
	if(posx!=0XFF && gpsx->fixmode>=2)gpsx->nshemi=*(p1+posx);					 
 	posx=Comma_Pos(p1,5);								//得到经度
	if(posx!=0XFF && gpsx->fixmode>=2)
	{												  
		temp=Str2num(p1+posx,&dx);		 	 
		gpsx->longitude=temp/Pow(10,dx+2);	//得到°
		rs=temp%Pow(10,dx+2);				//得到'		 
		gpsx->longitude=gpsx->longitude*Pow(10,5)+(rs*Pow(10,5-dx))/60;//转换为° 
	}
	posx=Comma_Pos(p1,6);								//东经还是西经
	if(posx!=0XFF && gpsx->fixmode>=2)gpsx->ewhemi=*(p1+posx);		 
	posx=Comma_Pos(p1,9);								//得到UTC日期
	if(posx!=0XFF)
	{
		temp=Str2num(p1+posx,&dx);		 				//得到UTC日期
		gpsx->utc.date=temp/10000;
		if(gpsx->utc.hour>=16) gpsx->utc.date++;
		gpsx->utc.hour=(gpsx->utc.hour+8)%24;			//国内时区修正
		gpsx->utc.month=(temp/100)%100;
		gpsx->utc.year=2000+temp%100;	 	 
	} 
}
//分析VTG信息
//gpsx:nmea信息结构体
//buf:接收到的GPS数据缓冲区首地址
void NMEA_VTG_Analysis(nmea_msg *gpsx,u8 *buf)
{
	u8 *p1,dx;			 
	u8 posx;    
	p1=(u8*)strstr((const char *)buf,"$GPVTG");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"$GNVTG");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"$GLVTG");
	if (p1==NULL) p1=(u8*)strstr((const char *)buf,"$GBVTG");
	posx=Comma_Pos(p1,7);								//得到地面速率
	if(posx!=0XFF)
	{
		gpsx->speed=Str2num(p1+posx,&dx);
		if(dx<3)gpsx->speed*=Pow(10,3-dx);	 	 		//确保扩大1000倍
	}
}  
//提取NMEA-0183信息
//gpsx:nmea信息结构体
//buf:接收到的GPS数据缓冲区首地址
void GPS_Analysis(nmea_msg *gpsx,u8 *buf)
{
	NMEA_GSA_Analysis(gpsx,buf);	//GPGSA解析,先得到定位类型
	NMEA_GSV_Analysis(gpsx,buf);	//GPGSV解析
	NMEA_GGA_Analysis(gpsx,buf);	//GPGGA解析 	
	NMEA_RMC_Analysis(gpsx,buf);	//GPRMC解析
	NMEA_VTG_Analysis(gpsx,buf);	//GPVTG解析
}

//GPS校验和计算
//buf:数据缓存区首地址
//len:数据长度
//cka,ckb:两个校验结果.
void Ublox_CheckSum(u8 *buf,u16 len,u8* cka,u8*ckb)
{
	u16 i;
	*cka=0;*ckb=0;
	for(i=0;i<len;i++)
	{
		*cka=*cka+buf[i];
		*ckb=*ckb+*cka;
	}
}
